// Provider pour le contexte de langue
export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('en');
  const [isLoading, setIsLoading] = useState(true);

  // Charger la langue sauvegard√©e ou d√©tecter la langue du navigateur
  useEffect(() => {
    const initializeLanguage = () => {
      try {
        // 1. V√©rifier localStorage
        const savedLanguage = localStorage.getItem('selectedLanguage');
        if (savedLanguage && translations[savedLanguage]) {
          setCurrentLanguage(savedLanguage);
          setIsLoading(false);
          return;
        }

        // 2. D√©tecter la langue du navigateur
        if (typeof window !== 'undefined') {
          const browserLang = navigator.language || navigator.languages[0];
          const langCode = browserLang.split('-')[0];

          if (translations[langCode]) {
            setCurrentLanguage(langCode);
          } else {
            setCurrentLanguage('en'); // Default to English
          }
        }

        setIsLoading(false);
      } catch (error) {
        console.error('Erreur lors de l\'initialisation de la langue:', error);
        setCurrentLanguage('fr');
        setIsLoading(false);
      }
    };

    initializeLanguage();
  }, []);

  // Fonction pour changer de langue
  const changeLanguage = useCallback((languageCode) => {
    if (translations[languageCode]) {
      setCurrentLanguage(languageCode);

      // Sauvegarder dans localStorage
      try {
        localStorage.setItem('selectedLanguage', languageCode);
      } catch (error) {
        console.error('Erreur lors de la sauvegarde de la langue:', error);
      }

      // √âmettre un √©v√©nement pour informer les autres composants
      window.dispatchEvent(new CustomEvent('languageChanged', {
        detail: { language: languageCode }
      }));
    }
  }, []);

  // Fonction de traduction avec fallback sur FR si cle non trouvee
  const t = useCallback((key, arg1, arg2) => {
    let fallback = key;
    let params = {};

    if (typeof arg1 === 'string') {
      fallback = arg1;
      if (arg2 && typeof arg2 === 'object') {
        params = arg2;
      }
    } else if (arg1 && typeof arg1 === 'object') {
      params = arg1;
      if (typeof arg2 === 'string') {
        fallback = arg2;
      }
    } else if (typeof arg1 !== 'undefined') {
      fallback = String(arg1);
    }

    if (typeof arg2 === 'object' && typeof arg1 !== 'object') {
      params = arg2;
    }

    if (typeof fallback !== 'string') {
      fallback = key;
    }

    // Helper pour chercher une traduction
    const findInTranslations = (langObj, searchKey) => {
      if (!langObj) return undefined;
      if (typeof langObj[searchKey] !== 'undefined') return langObj[searchKey];
      const keys = searchKey.split('.');
      let value = langObj;
      for (const k of keys) {
        value = value?.[k];
      }
      return value;
    };

    try {
      let template;

      // 1. Chercher dans la langue courante
      template = findInTranslations(translations[currentLanguage], key);

      // 2. Si pas trouve, fallback sur FR
      if (typeof template !== 'string' && currentLanguage !== 'fr') {
        template = findInTranslations(translations['fr'], key);
      }

      if (typeof template === 'object' && template !== null) {
        template = undefined;
      }

      if (typeof template !== 'string') {
        template = fallback;
      }

      if (typeof template !== 'string') {
        return key;
      }

      if (template.includes('{') && Object.keys(params).length > 0) {
        template = template.replace(/\{([^}]+)\}/g, (match, token) => {
          if (Object.prototype.hasOwnProperty.call(params, token)) {
            return params[token];
          }
          return match;
        });
      }

      return template;
    } catch (error) {
      console.error('Erreur lors de la traduction:', error);
      return fallback;
    }
  }, [currentLanguage]);

  // Fonction pour ajouter des traductions dynamiquement
  const addTranslations = useCallback((languageCode, newTranslations) => {
    if (translations[languageCode]) {
      translations[languageCode] = {
        ...translations[languageCode],
        ...newTranslations
      };
    } else {
      translations[languageCode] = newTranslations;
    }
  }, []);

  // Fonction pour obtenir toutes les langues disponibles
  const getAvailableLanguages = useCallback(() => {
    return Object.keys(translations).map(code => ({
      code,
      name: getLanguageName(code),
      nativeName: getLanguageNativeName(code)
    }));
  }, []);

  const value = {
    currentLanguage,
    changeLanguage,
    t,
    translations,
    addTranslations,
    getAvailableLanguages,
    isLoading
  };

  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-lime-500"></div>
      </div>
    );
  }

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

