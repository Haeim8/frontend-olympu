// Provider pour le contexte de langue
export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('fr');
  const [isLoading, setIsLoading] = useState(true);
  
  // Charger la langue sauvegard√©e ou d√©tecter la langue du navigateur
  useEffect(() => {
    const initializeLanguage = () => {
      try {
        // 1. V√©rifier localStorage
        const savedLanguage = localStorage.getItem('selectedLanguage');
        if (savedLanguage && translations[savedLanguage]) {
          setCurrentLanguage(savedLanguage);
          setIsLoading(false);
          return;
        }
        
        // 2. D√©tecter la langue du navigateur
        if (typeof window !== 'undefined') {
          const browserLang = navigator.language || navigator.languages[0];
          const langCode = browserLang.split('-')[0];
          
          if (translations[langCode]) {
            setCurrentLanguage(langCode);
          } else {
            setCurrentLanguage('fr'); // Langue par d√©faut
          }
        }
        
        setIsLoading(false);
      } catch (error) {
        console.error('Erreur lors de l\'initialisation de la langue:', error);
        setCurrentLanguage('fr');
        setIsLoading(false);
      }
    };
    
    initializeLanguage();
  }, []);
  
  // Fonction pour changer de langue
  const changeLanguage = useCallback((languageCode) => {
    if (translations[languageCode]) {
      setCurrentLanguage(languageCode);
      
      // Sauvegarder dans localStorage
      try {
        localStorage.setItem('selectedLanguage', languageCode);
      } catch (error) {
        console.error('Erreur lors de la sauvegarde de la langue:', error);
      }
      
      // √âmettre un √©v√©nement pour informer les autres composants
      window.dispatchEvent(new CustomEvent('languageChanged', {
        detail: { language: languageCode }
      }));
    }
  }, []);
  
  // Fonction de traduction
  const t = useCallback((key, fallback = key) => {
    try {
      const currentTranslations = translations[currentLanguage];
      if (!currentTranslations) {
        return fallback;
      }
      
      // Chercher d'abord dans les cl√©s √† points
      if (currentTranslations[key]) {
        return currentTranslations[key];
      }
      
      // Puis essayer la navigation par points
      const keys = key.split('.');
      let value = currentTranslations;
      
      for (const k of keys) {
        value = value?.[k];
      }
      
      return value || fallback;
    } catch (error) {
      console.error('Erreur lors de la traduction:', error);
      return fallback;
    }
  }, [currentLanguage]);
  
  // Fonction pour ajouter des traductions dynamiquement
  const addTranslations = useCallback((languageCode, newTranslations) => {
    if (translations[languageCode]) {
      translations[languageCode] = {
        ...translations[languageCode],
        ...newTranslations
      };
    } else {
      translations[languageCode] = newTranslations;
    }
  }, []);
  
  // Fonction pour obtenir toutes les langues disponibles
  const getAvailableLanguages = useCallback(() => {
    return Object.keys(translations).map(code => ({
      code,
      name: getLanguageName(code),
      nativeName: getLanguageNativeName(code)
    }));
  }, []);
  
  const value = {
    currentLanguage,
    changeLanguage,
    t,
    translations,
    addTranslations,
    getAvailableLanguages,
    isLoading
  };
  
  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-lime-500"></div>
      </div>
    );
  }
  
  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

